    /**
     * Import Excel the specified resource from storage.
     *
     * @return \Illuminate\Http\Response
     */
    public function import(Request $request)
    {

        // validasi
		$this->validate($request, [
			'file' => 'required|mimes:csv,xls,xlsx'
		]);
 
		// menangkap file excel
		$file = $request->file('file');
 
		// membuat nama file unik
		$nama_file = rand().$file->getClientOriginalName();
 
		// upload ke folder file_pasien di dalam folder public
		$file->move('file_pasien',$nama_file);
 
		// import data
		Excel::import(new PasienImport, public_path('/file_pasien/'.$nama_file));
 
		// alihkan halaman kembali
        return redirect('/')->with('success','Sukses Import Data Pasien');
    }
